#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int	*arr;
	int	i;

	if (min >= max)
		return (NULL);
	arr = (int *) malloc((max - min) * 4);
	i = -1;
	while (min < max)
		arr[++i] = min++;
	return (arr);
}
