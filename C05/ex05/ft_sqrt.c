int	ft_sqrt(int nb)
{
	unsigned int	r;

	if (nb <= 0)
		return (0);
	r = 1;
	while ((r * r) <= (unsigned int)nb)
	{
		if ((r * r) == (unsigned int) nb)
			return (r);
		r++;
	}
	return (0);
}
